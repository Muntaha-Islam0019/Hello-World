# ----- For Loop -----

print("\n")

priceOfCrates = [1000, 200, 300, 4000]

for i in priceOfCrates:
    print(i)

print("\n")

for i in range(10):  # Starts with 0.
    print(i)

print("\n")

for i in range(7, 13):  # See, it does not print 13.
    print(i)

print("\n")

for i in range(2, 10, 2):  # With double steps.
    print(i)
