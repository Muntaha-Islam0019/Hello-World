# Set is auto-sorted & non-repeating data structure in Python, kinda like maps.

a_list = ['some', 'values', 'well', "let's", 'try', 'mixing', 19, 47, 1]
a_set = set(a_list)

print(a_set)

# Sets can be explicitly created. Values are gonna be autmatically sorted.
another_set = {3, 2, 4, 5, 1}
print(another_set)
